    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int numero = Integer.parseInt(this.txtNumero.getText());
        EstadoTicket tipo = tipoTicket();
        cont++;
        this.tick = new Ticket(numero,tipo);
        
        this.tickets.agregar(tick);
        this.btnAgregar.setEnabled(false);
    }//GEN-LAST:event_btnAgregarActionPerformed

    public EstadoTicket tipoTicket() {
        EstadoTicket tipo;
        switch (String.valueOf(this.txtTipo)) {
            case "Plataforma" -> {
                 return tipo = EstadoTicket.PLATAFORMA;
            }
            case "Cajas" -> {
                return tipo = EstadoTicket.CAJAS;
            }
            default -> {
                return tipo = EstadoTicket.PREFERENCIAL;
            }
        }
    }

